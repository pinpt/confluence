package html

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestEmoji(t *testing.T) {
	assert := assert.New(t)
	buf, err := Parse(`:)`)
	assert.NoError(err)
	assert.Equal("ğŸ˜ƒ", buf)
	buf, err = Parse(`:(`)
	assert.NoError(err)
	assert.Equal("ğŸ™", buf)
	buf, err = Parse(`:P`)
	assert.NoError(err)
	assert.Equal("ğŸ˜›", buf)
	buf, err = Parse(`:D`)
	assert.NoError(err)
	assert.Equal("ğŸ˜", buf)
	buf, err = Parse(`;)`)
	assert.NoError(err)
	assert.Equal("ğŸ˜‰", buf)
	buf, err = Parse(`(y)`)
	assert.NoError(err)
	assert.Equal("ğŸ‘", buf)
	buf, err = Parse(`(n)`)
	assert.NoError(err)
	assert.Equal("ğŸ‘", buf)
	buf, err = Parse(`(i)`)
	assert.NoError(err)
	assert.Equal("â„¹ï¸", buf)
	buf, err = Parse(`(/)`)
	assert.NoError(err)
	assert.Equal("âœ…", buf)
	buf, err = Parse(`(x)`)
	assert.NoError(err)
	assert.Equal("â—ï¸", buf)
	buf, err = Parse(`(!)`)
	assert.NoError(err)
	assert.Equal("âš ï¸", buf)
	buf, err = Parse(`(+)`)
	assert.NoError(err)
	assert.Equal("â•", buf)
	buf, err = Parse(`(-)`)
	assert.NoError(err)
	assert.Equal("â–", buf)
	buf, err = Parse(`(?)`)
	assert.NoError(err)
	assert.Equal("â“", buf)
}
